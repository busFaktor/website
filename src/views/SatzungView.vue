      <h1>Satzung des Vereins busFaktor()</h1>
      <p>Stand: 15.09.2020</p>
      <h2>§ 1 Der Verein</h2>
      <ol>
        <li>
          Der Verein führt den Namen busFaktor(). Er soll in das Vereinsregister
          eingetragen werden und führt danach den Zusatz „e.V.".
        </li>
        <li>Der Verein hat seinen Sitz in Augsburg.</li>
        <li>Der Verein ist in Deutschland aktiv und kooperiert global.</li>
        <li>Das Geschäftsjahr ist das Kalenderjahr.</li>
        <li>Der Verein ist politisch neutral.</li>
        <li>Der Verein kann Untergliederungen errichten.</li>
        <li>
          Unsere Werte sind Demokratie, Transparenz, Vielfalt, Offenheit und
          Gleichberechtigung
        </li>
      </ol>
      <h2>§ 2 Zweck und Gemeinnützigkeit</h2>
      <ol>
        <li>
          Der Verein fördert und unterstützt Vorhaben der Bildung, der
          Volksbildung und die Förderung des bürgerschaftlichen Engagements
          zugunsten gemeinnütziger Zwecke.
        </li>
        <li>
          Der Verein verfolgt ausschließlich und unmittelbar gemeinnützige
          Zwecke im Sinne des Abschnitts „steuerbegünstigte Zwecke“ der
          Abgabenordnung 1977 (§51ff AO) in der jeweils gültigen Fassung; er
          dient ausschließlich und unmittelbar der Bildung, der Volksbildung,
          der Förderung des bürgerschaftlichen Engagements und dem Nutzen der
          Allgemeinheit.
        </li>
        <li>
          Er darf keine Gewinne erzielen, er ist selbstlos tätig und verfolgt
          nicht in erster Linie eigenwirtschaftliche Zwecke. Die Mittel des
          Vereins werden ausschließlich und unmittelbar zu den satzungsgemäßen
          Zwecken verwendet.
        </li>
        <li>
          Die Mitglieder erhalten keine Zuwendungen aus den Mitteln des Vereins.
          Niemand darf durch Ausgaben, die dem Zwecke des Clubs fremd sind oder
          durch unverhältnismäßig hohe Vergütungen begünstigt werden.
        </li>
      </ol>
      <h2>§ 3 Umsetzung und Verwirklichung des Satzungszwecks</h2>
      <p>
        Der Vereinszweck soll unter anderem durch folgende Mittel verwirklicht
        werden:
      </p>
      <ol>
        <li>
          Entwicklung und Verbreitung von freier und Open Source Software zur
          Nutzung durch gemeinnützige Organisationen
        </li>
        <li>Veröffentlichung von Quellcode als Referenz- und Lernmaterial</li>
        <li>
          Veröffentlichung von Anleitungen und Lerninhalten zur
          Softwareentwicklung und -bereitstellung, z.B. in Git-Repositories
        </li>
        <li>
          Bereitstellung von Software und technische Unterstützung für
          gemeinnützige Organisationen, z.B. durch Beratung
        </li>
        <li>
          öffentliche Aufklärung über Themen des Datenschutzes und der
          Informationssicherheit, z.B. in Form von Blogartikeln
        </li>
        <li>
          Erfahrungsaustausch und Wissensvermittlung im Rahmen von öffentlich
          zugänglichen Videokonferenzen zum gemeinsamen Programmieren
          (Pair-Programming) an freien Softwareprojekten
        </li>
        <li>
          Teilnahme an Treffen und öffentlichen Informationsveranstaltungen
        </li>
      </ol>
      <h2>§ 4 Formen der Mitgliedschaft</h2>
      <ol>
        <li>
          Aktives Mitglied des Vereins kann jede natürliche Person werden, die
          im Einvernehmen mit dem Vorstand an der selbstständigen Planung und
          Durchführung einer Initiative gemäß der §§ 2 und 3 beteiligt ist. Die
          aktive Mitgliedschaft besteht grundsätzlich in der Teilnahme an
          Initiativen gemäß der §§ 2 und 3.
        </li>
        <li>
          Die Mitgliedschaft einer natürlichen Person ist nicht mehr als aktiv
          anzusehen, wenn diese länger als 4 Monate nicht mehr an den
          Aktivitäten des Vereins teilgenommen hat. Über die weitere
          Mitgliedschaft entscheidet in solch einem Fall der Vorstand.
        </li>
        <li>
          Förderndes Mitglied kann jede natürliche oder juristische Person
          werden, die das Ziel des Vereins gemäß §§ 2 und 3 unterstützt. Die
          fördernde Mitgliedschaft besteht grundsätzlich in der finanziellen und
          materiellen Unterstützung der Vereinsarbeit.
        </li>
      </ol>
      <h2>§ 5 Rechte und Pflichten der Mitglieder</h2>
      <ol>
        <li>
          Fördernde Mitglieder haben das Recht, Initiativen vorzuschlagen und
          Rechenschaft vom Vorstand in Hinsicht auf das Vereinsvermögen zu
          verlangen.
        </li>
        <li>
          Aktive Mitglieder haben alle Rechte des fördernden Mitglieds sowie das
          Recht, dem Vorstand die Entlastung auszusprechen oder zu verweigern,
          über alle Tätigkeiten des Vorstands im Sinne der §§ 2 und 3 zu beraten
          und in der Mitgliederversammlung abzustimmen.
        </li>
        <li>
          Alle Mitglieder werden regelmäßig über die Vorgänge des Vereins
          informiert und zu Veranstaltungen und Aktivitäten, sowie zur
          Mitgliederversammlung des Vereins eingeladen.
        </li>
        <li>
          Jedes Mitglied hat die Pflicht, die Interessen des Vereins zu fördern,
          insbesondere regelmäßig, jährlich vorab, seine Mitgliedsbeiträge zu
          leisten und, soweit es in seinen Kräften steht, das Vereinsleben durch
          seine Mitarbeit zu unterstützen.
        </li>
        <li>
          Jedes Mitglied ist verpflichtet die jeweils gültigen und von der
          Mitgliederversammlung beschlossenen Datenschutzbestimmungen des
          Vereins zu beachten.
        </li>
      </ol>
      <h2>§ 6 Beginn und Ende der Mitgliedschaft</h2>
      <ol>
        <li>
          Die Aufnahme in den Verein ist schriftlich beim Vorstand zu
          beantragen. Bei Minderjährigen ist der Aufnahmeantrag durch die
          gesetzlichen Vertreter zu stellen. Der Vorstand entscheidet über den
          Aufnahmeantrag nach freiem Ermessen. Eine Ablehnung des Antrags muss
          er gegenüber dem Antragsteller nicht begründen. Die Aufnahme ist
          gültig, wenn sie vom Vorstand schriftlich anerkannt ist.
        </li>
        <li>
          Die Mitgliedschaft im Verein endet durch Tod (bei juristischen
          Personen mit deren Erlöschen), Austritt oder Ausschluss.
        </li>
        <li>
          Der Austritt ist schriftlich gegenüber dem Vorstand zu erklären.
        </li>
        <li>
          Der Austritt erfolgt unmittelbar mit der schriftlichen
          Willenserklärung des Mitglieds.
        </li>
        <li>
          Der Ausschluss erfolgt durch den Ausschlussbeschluss des Vorstands
          oder, sofern er sich gegen den Vorstand selbst bezieht, durch den
          anderen Vorstand im Verein mit den aktiven Mitgliedern des Vereins.
          Ausschlussgrund ist die wiederholte schwerwiegende Verletzung der
          Interessen des Vereins, insbesondere der missbräuchliche Umgang mit
          den Mitteln des Vereinsvermögens. Im Falle einer besonders
          schwerwiegenden Verletzung ist die Wiederholung keine Voraussetzung.
          Besonders schwerwiegend ist eine Verletzung, die den Ruf, den Bestand
          oder die Tätigkeit des Vereins unmittelbar gefährdet.
        </li>
        <li>
          Ein Mitglied kann durch Beschluss des Vorstands aus dem Verein
          ausgeschlossen werden, wenn es mehr als drei Monate mit der Zahlung
          seiner Aufnahmegebühr oder seiner Mitgliedsbeiträge im Rückstand ist
          und trotz schriftlicher Mahnung unter Androhung des Ausschlusses die
          Rückstände nicht eingezahlt hat.
        </li>
        <li>
          Gezahlte Mitgliedsbeiträge werden bei Ende der Mitgliedschaft nicht
          erstattet.
        </li>
      </ol>
      <h2>§ 7 Mittel, Aufnahmegebühr und Mitgliedsbeiträge</h2>
      <ol>
        <li>
          Mittel des Vereins dürfen nur für die satzungsmäßigen Zwecke verwendet
          werden. Die Mitglieder erhalten keine Zuwendungen aus den Mitteln des
          Vereins.
        </li>
        <li>
          Es darf keine Person durch Ausgaben, die dem Zweck des Vereins fremd
          sind, oder durch unverhältnismäßig hohe Vergütungen begünstigt werden.
        </li>
        <li>
          Jedes Mitglied hat einen im Voraus fällig werdenden jährlichen
          Mitgliedsbeitrag zu entrichten.
        </li>
        <li>
          Die Höhe der Aufnahmegebühr und der Mitgliedsbeiträge wird von der
          Mitgliederversammlung festgelegt.
        </li>
      </ol>
      <h2>§ 8 Organe des Vereins</h2>
      <ol>
        <li>
          Organe des Vereins sind die Mitgliederversammlung, der Vorstand und
          ggf. der Aufsichtsrat.
        </li>
        <li>
          Die Mitgliederversammlung entscheidet über die Bestellung eines
          Aufsichtsrates. So lange der Verein weniger als 20 aktive Mitglieder
          hat, kann auf die Wahl eines Aufsichtsrates verzichtet werden.
        </li>
        <li>
          Weitere Organe können durch Beschluss des Vorstands eingerichtet
          werden.
        </li>
      </ol>
      <h2>§ 9 Vorstand</h2>
      <ol>
        <li>
          Der Vorstand besteht aus dem Vorsitzenden und seinem Stellvertreter.
        </li>
        <li>
          Der Vorsitzende und sein Stellvertreter vertreten den Verein jeweils
          allein.
        </li>
        <li>
          Den Mitgliedern des Vorstands kann eine Vergütung gezahlt werden. Über
          die Höhe der Vergütung entscheidet die Mitgliederversammlung.
          <h2>§ 10 Aufgaben des Vorstands</h2>
        </li>
        <li>
          Dem Vorstand des Vereins obliegen die Vertretung des Vereins nach § 26
          BGB und die Führung seiner Geschäfte. Er hat insbesondere folgende
          Aufgaben: a) die Einberufung und Vorbereitung der
          Mitgliederversammlungen einschließlich der Aufstellung der
          Tagesordnung b) die Ausführung von Beschlüssen der
          Mitgliederversammlung c) die Verwaltung des Vereinsvermögens und die
          Anfertigung des Jahresberichts d) die Aufnahme neuer Mitglieder
        </li>
        <li>
          Der Vorstand ist dem Aufsichtsrat Rechenschaft schuldig und hat in der
          Aufsichtsratssitzung auf Anfrage einen schriftlichen oder mündlichen
          Bericht über die Beschlüsse und Tätigkeiten des Vorstands vorzulegen.
        </li>
      </ol>
      <h2>§ 11 Bestellung des Vorstands</h2>
      <ol>
        <li>
          Die Mitglieder des Vorstands werden von der Mitgliederversammlung für
          die Dauer von zwei Jahren einzeln gewählt. Mitglieder des Vorstands
          können nur Mitglieder des Vereins sein; mit der Mitgliedschaft im
          Verein endet auch die Mitgliedschaft im Vorstand.
        </li>
        <li>
          Die Wiederwahl oder die vorzeitige Abberufung eines Mitglieds durch
          die Mitgliederversammlung ist zulässig. Ein Mitglied bleibt nach
          Ablauf der regulären Amtszeit bis zur Wahl seines Nachfolgers im Amt.
        </li>
        <li>
          Scheidet ein Mitglied vorzeitig aus dem Vorstand aus, so sind die
          verbleibenden Mitglieder des Vorstands berechtigt, ein Mitglied des
          Vereins bis zur Wahl des Nachfolgers durch die Mitgliederversammlung
          in den Vorstand zu wählen.
        </li>
      </ol>
      <h2>§ 12 Beratung und Beschlussfassung des Vorstands</h2>
      <ol>
        <li>
          Der Vorstand tritt nach Bedarf vor Ort oder online zusammen. Die
          Sitzungen werden vom Vorsitzenden, bei dessen Verhinderung von seinem
          Stellvertreter, einberufen. Eine Einberufungsfrist von einer Woche
          soll eingehalten werden.
        </li>
        <li>
          Der Vorstand ist beschlussfähig, wenn mindestens die Hälfte der
          Mitglieder anwesend sind. Bei der Beschlussfassung entscheidet die
          Mehrheit der abgegebenen gültigen Stimmen. Bei Stimmengleichheit
          entscheidet die Stimme des Vorsitzenden, bei dessen Verhinderung die
          seines Stellvertreters.
        </li>
        <li>
          Die Beschlüsse des Vorstands sind zu protokollieren. Das Protokoll ist
          vom Protokollführer sowie vom Vorsitzenden, bei dessen Verhinderung
          von seinem Stellvertreter oder einem anderen Mitglied des Vorstands zu
          unterschreiben. Die Unterschrift erfolgt händisch oder schriftlich
          nach der europäischen eIDAS Verordnung (Verordnung Nr. 910/2014 des
          Europäischen Parlaments und des Rates über elektronische
          Identifizierung und Vertrauensdienste für elektronische Transaktionen
          im Binnenmarkt und zur Aufhebung der Richtlinie 1999/93/EG)
        </li>
      </ol>
      <h2>§ 13 Aufgaben der Mitgliederversammlung</h2>
      <p>
        Die Mitgliederversammlung ist zuständig für die Entscheidungen in
        folgenden Angelegenheiten:
      </p>
      <p>
        a) Änderungen der Satzung b) die Festsetzung der Aufnahmegebühr und der
        Mitgliedsbeiträge c) der Ausschluss von Mitgliedern aus dem Verein d)
        die Wahl und die Abberufung der Mitglieder des Vorstands e) die
        Einrichtung eines Aufsichtsrates sowie die Wahl und Abberufung dessen
        Mitglieder f) die Entgegennahme des Jahresberichts und die Entlastung
        des Vorstands g) die Auflösung des Vereins
      </p>
      <h2>§ 14 Einberufung der Mitgliederversammlung</h2>
      <ol>
        <li>
          Mindestens einmal im Jahr, möglichst im ersten Quartal, ist vom
          Vorstand eine ordentliche Mitgliederversammlung einzuberufen. Die
          Einberufung erfolgt schriftlich unter Einhaltung einer Frist von zwei
          Wochen und unter Angabe der Tagesordnung.
        </li>
        <li>
          Die Tagesordnung setzt der Vorstand fest. Jedes Vereinsmitglied kann
          bis spätestens eine Woche vor der Mitgliederversammlung beim Vorstand
          schriftlich eine Ergänzung der Tagesordnung beantragen. Über den
          Antrag entscheidet der Vorstand. Über Anträge zur Tagesordnung, die
          vom Vorstand nicht aufgenommen wurden oder die erstmals in der
          Mitgliederversammlung gestellt werden, entscheidet die
          Mitgliederversammlung mit der Mehrheit der Stimmen der anwesenden
          Mitglieder; dies gilt nicht für Anträge, die eine Änderung der
          Satzung, Änderungen der Mitgliedsbeiträge oder die Auflösung des
          Vereins zum Gegenstand haben.
        </li>
        <li>
          Der Vorstand hat eine außerordentliche Mitgliederversammlung
          einzuberufen, wenn es das Interesse des Vereins erfordert oder wenn
          mindestens ein Zehntel der aktiven Mitglieder dies schriftlich unter
          Angabe des Zwecks und der Gründe beantragt.
        </li>
        <li>Die Mitgliederversammlung kann online abgehalten werden.</li>
        <li>
          Ist der Vorstand verhindert oder nicht arbeitsfähig, kann mit
          Zustimmung von 1/10 der Mitglieder eine außerordentliche
          Mitgliederversammlung einberufen werden.
        </li>
      </ol>
      <h2>§ 15 Beschlussfassung der Mitgliederversammlung</h2>
      <ol>
        <li>
          Die Mitgliederversammlung wird vom Vorsitzenden des Vorstands, bei
          dessen Verhinderung von seinem Stellvertreter und bei dessen
          Verhinderung von einem durch die Mitgliederversammlung zu wählenden
          Versammlungsleiter geleitet.
        </li>
        <li>
          Die Mitgliederversammlung ist beschlussfähig, wenn mindestens ein
          Drittel aller aktiven Vereinsmitglieder anwesend ist. Bei
          Beschlussunfähigkeit ist der Vorstand verpflichtet, innerhalb von vier
          Wochen eine zweite Mitgliederversammlung mit der gleichen Tagesordnung
          einzuberufen. Diese ist ohne Rücksicht auf die Zahl der erschienenen
          Mitglieder beschlussfähig. Hierauf ist in der Einladung hinzuweisen.
        </li>
        <li>
          Die Mitgliederversammlung beschließt in offener Abstimmung mit der
          Mehrheit der Stimmen der anwesenden aktiven Mitglieder. Kann bei
          Wahlen kein Kandidat die Mehrheit der Stimmen der anwesenden aktiven
          Mitglieder auf sich vereinen, ist gewählt, wer die Mehrheit der
          abgegebenen gültigen Stimmen erhalten hat; zwischen mehreren
          Kandidaten ist eine Stichwahl durchzuführen. Beschlüsse über eine
          Änderung der Satzung bedürfen einer Dreiviertelmehrheit, der Beschluss
          über die Änderung des Zwecks oder die Auflösung des Vereins der
          Zustimmung von neun Zehnteln der anwesenden aktiven Mitglieder.
        </li>
        <li>
          Über den Ablauf der Mitgliederversammlung und die gefassten Beschlüsse
          ist ein Protokoll zu fertigen, das vom Protokollführer und vom
          Versammlungsleiter zu unterschreiben ist. Die Unterschrift erfolgt
          händisch oder schriftlich nach der europäischen eIDAS Verordnung
          (Verordnung Nr. 910/2014 des Europäischen Parlaments und des Rates
          über elektronische Identifizierung und Vertrauensdienste für
          elektronische Transaktionen im Binnenmarkt und zur Aufhebung der
          Richtlinie 1999/93/EG)
        </li>
      </ol>
      <h2>§ 16 Aufsichtsrat</h2>
      <ol>
        <li>
          Der Aufsichtsrat besteht aus drei gleichberechtigten Mitgliedern. Die
          Mitglieder werden einzeln auf die Dauer von einem Jahr von der
          Mitgliederversammlung gewählt. Die Wiederwahl oder die vorzeitige
          Abberufung eines Mitglieds durch die Mitgliederversammlung ist
          zulässig. Ein Mitglied bleibt nach Ablauf der regulären Amtszeit bis
          zur Wahl eines Nachfolgers im Amt.
        </li>
        <li>
          Scheidet ein Mitglied vorzeitig aus dem Aufsichtsrat aus, so sind die
          verbleibenden Mitglieder des Aufsichtsrats berechtigt, ein Mitglied
          des Vereins bis zur Wahl des Nachfolgers durch die
          Mitgliederversammlung in den Aufsichtsrat zu wählen.
        </li>
        <li>
          Nur aktive Mitglieder des Vereins können Mitglieder im Aufsichtsrat
          sein. Vorstandsmitglieder können nicht zugleich Mitglieder im
          Aufsichtsrat sein.
        </li>
      </ol>
      <h2>§ 17 Aufgaben des Aufsichtsrats</h2>
      <ol>
        <li>
          Der Aufsichtsrat vertritt den Verein gegenüber dem Vorstand,
          insbesondere bei der Verhandlung, Unterzeichnung und Kündigung von
          Dienstverträgen.
        </li>
        <li>
          Er berät, begleitet und kontrolliert den Vorstand bei der Umsetzung
          der Beschlüsse der Mitgliederversammlung. Dabei hat der Aufsichtsrat
          ein Recht auf Auskunft und jederzeitige Einsicht in die
          Geschäftsunterlagen des Vereins.
        </li>
        <li>
          Die Mitglieder des Aufsichtsrates haften gegenüber dem Verein nur im
          Fall vorsätzlicher oder grob fahrlässiger Schädigung.
        </li>
      </ol>
      <h2>§ 18 Beratung und Beschlussfassung des Aufsichtsrats</h2>
      <ol>
        <li>
          Der Aufsichtsrat tritt nach Bedarf vor Ort oder online zusammen,
          mindestens jedoch einmal pro Jahr. Alle Aufsichtsratsmitglieder und
          Mitglieder des Vorstandes müssen eingeladen werden. Eine
          Einberufungsfrist von einer Woche soll eingehalten werden.
        </li>
        <li>
          Der Aufsichtsrat ist beschlussfähig, wenn mindestens zwei Mitglieder
          anwesend sind. Bei der Beschlussfassung entscheidet die Mehrheit der
          abgegebenen gültigen Stimmen. Bei Stimmengleichheit gilt ein Antrag
          als abgelehnt.
        </li>
        <li>
          Beschlüsse müssen protokolliert und der Mitgliederversammlung
          vorgelegt werden. Schriftliche, fernmündliche oder elektronische
          Beschlussfassungen sind zulässig.
        </li>
      </ol>
      <h2>
        § 19 Auflösung des Vereins, Beendigung aus anderen Gründen, Wegfall
        steuerbegünstigter Zwecke
      </h2>
      <ol>
        <li>
          Im Falle der Auflösung des Vereins sind der Vorsitzende des Vorstands
          und sein Stellvertreter gemeinsam vertretungsberechtigte Liquidatoren,
          falls die Mitgliederversammlung keine anderen Personen beruft.
        </li>
        <li>
          Bei Auflösung oder Aufhebung des Vereins oder bei Wegfall
          steuerbegünstigter Zwecke fällt das Vermögen des Vereins an eine
          juristische Person des öffentlichen Rechts oder eine andere
          steuerbegünstigte Körperschaft, zwecks Verwendung für einen
          gemeinnützigen Zweck, im weiteren Sinne im Bereich der Computertechnik
          oder Netzwerke.
        </li>
        <li>
          Die vorstehenden Bestimmungen gelten entsprechend, wenn dem Verein die
          Rechtsfähigkeit entzogen wurde.
        </li>
      </ol>
